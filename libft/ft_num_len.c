int		ft_num_len(char *s)
{
	int len;

	len = 0;
	if (!s)
		return (len);
	while ((s[len] >= 9 && s[len] <= 13) || s[len] == ' ')
		len++;
	if (s[len] == '-' || s[len] == '+')
		len++;
	while (s[len] >= '0' && s[len] <= '9')
		len++;
	return (len);
}
